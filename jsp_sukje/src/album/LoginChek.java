package album;

public class LoginChek {
	int loginChk;
	String id;
	
	
	public LoginChek(int loginChk, String id) {
		this.loginChk = loginChk;
		this.id = id;
	}
	public int getLoginChk() {
		return loginChk;
	}
	public void setLoginChk(int loginChk) {
		this.loginChk = loginChk;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}

	
}
